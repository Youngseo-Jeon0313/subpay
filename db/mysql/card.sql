CREATE TABLE card (
      id BIGINT AUTO_INCREMENT PRIMARY KEY,
      userId BIGINT NOT NULL,
      cardNumber BIGINT NOT NULL,
      expirationDate DATETIME NOT NULL,
      cvv INT NOT NULL,
      balance BIGINT NOT NULL DEFAULT 0,
      priority INT NOT NULL DEFAULT 0,
);

